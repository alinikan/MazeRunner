
package com.project.group13.backend.model;

/**
 * Directions enum class,
 * holds different directions objects can go in the game
 * 
 * @author Ali Nikan
 * @author Kelvin Lu
 * @author Steven Wong
 * @author Shaima El Masry
 * @version 1.0 
 */
public enum Directions {
    /** Indicates upward movement or orientation. */
    UP,

    /** Indicates downward movement or orientation. */
    DOWN,

    /** Indicates leftward movement or orientation. */
    LEFT,

    /** Indicates rightward movement or orientation. */
    RIGHT,

    /** Indicates no movement or neutral orientation. */
    NULL
}
